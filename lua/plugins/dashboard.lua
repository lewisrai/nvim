return {
	"nvimdev/dashboard-nvim",
	dependencies = {
		"nvim-tree/nvim-web-devicons",
	},
	config = function()
		local load_logo = function()
			local logo_left = vim.split(logo1, "\n")
			local logo_right = vim.split(logo2, "\n")

			for i = 1, 25 do
				logo_left[i] = logo_left[i] .. logo_right[i]
			end

			vim.api.nvim_set_hl(0, "DashboardHeader", { fg = require("tokyonight.colors").setup().purple })

			return logo_left
		end

		local telescope_find_file = function()
			vim.api.nvim_exec("ene", false)
			vim.api.nvim_exec("Neotree filesystem reveal left", false)
			require("telescope.builtin").find_files()
		end

		local new_file = function()
			vim.api.nvim_exec("ene | startinsert", false)
			vim.api.nvim_exec("Neotree filesystem reveal left show", false)
		end

		local cd = function()
			vim.api.nvim_input(":cd ")
		end

		local config = function()
			vim.api.nvim_set_current_dir("C:/Users/Raiwin/Appdata/Local/nvim")
		end

		require("dashboard").setup({
			theme = "doom",
			hide = {
				statusline = false,
			},
			config = {
				disable_move = true,
				header = load_logo(),
				center = {
					{ action = telescope_find_file, desc = " Find File", icon = " ", key = "f" },
					{ action = new_file, desc = " New File", icon = " ", key = "n" },
					{ action = cd, desc = " Go To Directory", icon = " ", key = "g" },
					{ action = "Lazy", desc = " Lazy", icon = "󰒲 ", key = "l" },
					{ action = config, desc = " Config", icon = " ", key = "c" },
					{ action = "qa", desc = " Quit", icon = " ", key = "q" },
				},
				footer = function()
					local stats = require("lazy").stats()
					local ms = (math.floor(stats.startuptime * 100 + 0.5) / 100)
					return {
						"",
						"⚡Neovim loaded " .. stats.loaded .. "/" .. stats.count .. " plugins in " .. ms .. "ms",
						"",
						"u r reading this rn",
					}
				end,
			},
		})
	end,
}
